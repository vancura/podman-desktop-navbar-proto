/**
 * Creates an SVG icon element.
 * @param name - The icon name.
 * @param variant - The icon variant (outline or filled).
 * @param size - The icon size in pixels.
 * @param color - The icon color.
 * @returns SVG group element containing the icon.
 */
export function createIcon(
    name: IconName,
    variant: IconVariant = 'outline',
    size = 24,
    color = 'currentColor',
): SVGGElement {
    const icon = ICONS[name];
    const group = createSvgElement('g', {
        'data-icon': name,
        'data-variant': variant,
    });

    const path = createSvgElement('path', {
        d: icon[variant],
        fill: variant === 'filled' ? color : 'none',
        stroke: variant === 'outline' ? color : 'none',
        'stroke-width': variant === 'outline' ? '1.5' : '0',
        'stroke-linecap': 'round',
        'stroke-linejoin': 'round',
    });

    // Scale the icon to the desired size
    const scale = size / 24;
    if (scale !== 1) {
        group.setAttribute('transform', `scale(${scale})`);
    }

    group.appendChild(path);
    return group;
}

/**
 * Gets the SVG path data for an icon.
 * @param name - The icon name.
 * @param variant - The icon variant.
 * @returns The SVG path data string.
 */
export function getIconPath(name: IconName, variant: IconVariant = 'outline'): string {
    return ICONS[name][variant];
}
